            <div class="open">
               <h2 class="question">Liaison with International Bodies</h2>
               <span class="faq-t"></span>
               <p class="answer">
               The AIU has close links with the following agencies: University Grants Commission; Indian Council for Agricultural Research; Department of Secondary and Higher Education, Ministry of Human Resource Development; Ministry of Youth Affairs and Sports; Indian Council for Cultural Relations; National Assessment and Accreditation Council; Distance Education Council; All India Council for Technical Education; National Council for Teacher Education; Education Departments of various States and State Councils of Higher Education. At the time of its annual meeting, the AIU organises a joint meeting of Vice Chancellors/ Directors of member Universities/Institutions with the heads of apex bodies to consider important policy matters concerning higher education.<br><br>

               The AIU maintains close relationship with international organisations in the field of higher education including the Commonwealth of Learning, Association of Commonwealth Universities, International Association of Universities and the Commonwealth Secretariat. In recent years, it has signed Memorandums of Understanding with Hochschul Rektoren Konferenz (HRK), Germany; Australian Vice Chancellors’ Committee (AVCC), and the Committee of Vice Chancellors and Directors (CVCD) of Sri Lanka, for mutual cooperation in various areas of higher education.<br><br>
               
               The Association has recently signed a Memorandum of Understanding (MoU) with the International Bank for Reconstruction and Development acting through its World Bank Institute for Collaboration on learning and knowledge sharing programme for “Youth Leadership on HIV/AIDS”, the programme initially is for a period of three years.<br><br>
               
               As a beginning, a special session on “University Action on HIV/AIDS” for Vice Chancellors/ Directors of member Universities/Institutions was held during the Annual Meeting of the Association held at Sri Venkateswara University, Tirupati in November, 2003.<br>
        
