         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Tryst with Health Science Education - 2006 (Academics and Beyond)</h1>
         <p class="underdiv">
            This volume presents convocation addresses delivered in the universities, university level institutions and institutes of national importance in Health Sciences, over a period of fifteen years. These priceless pieces contains in themselves something unique and have become guidelines for the future generations. The issues deliberated in this volume range from role of Medicine in the social and economic development of the country, medical ethics, patient-care, doctor-patient relationship, reflection of a doctor, societal relevance, equity, access to health care facilities and social justice to new frontiers in Medical Sciences. The convocation speakers have given quintessence of their experiences not only to the medical graduates but also to the society as a whole.
            <br><strong>Pages : 278 + xxii Price : Rs. 275.00 (Paper Back) US $ : 30 ISBN : 81-7520-096-0</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Handbook on Engineering Education - 2008</h1>
         <p class="underdiv">
            The 11th edition of "Handbook on Engineering Education" contains State-wise information on 1045 colleges/institutes/university departments in the country including 171 new institutions added since the publication of the previous edition. Details on All India Engineering Entrance Examination (AIEEE) conducted by the CBSE are covered for admission to BE/ B.Tech. courses. The information in institutions in the Handbook includes: Year of Establishment of department/institute: name of its Principal/Director probable date of notification/last date of application; number of seats available in each engineering/ technology branch; seats for NRIs/foreign students; liability; application procedure; state-wise common entrance test rules for BE/B. Tech. Courses; fees; hostel facilities etc.
            <br><strong>Pages: 528 Price : Rs. 400.00 (Paper Back) US $ : 40 ISBN : 81-7520-103-7</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Handbook on Health Sciences Education - 2009</h1>
         <p class="underdiv">
            The 17th edition of "Handbook on Health Sciences Education" contains State-Wise information on 676 colleges/institutes/university departments in the Country. Details of All India Medical Entrance Examination (AIMEE) Conducted by the CBSE are covered for admission to MBBS/BDS courses. The information on Institutions in the Handbook includes: year of Establishment of department/institute; name of its Principal/Director; probable date of notification/ last date application; number of seats available in each medical branch; seats for NRIs/ foreign students; eligibility; application procedure; state-wise common entrance test rules for health sciences courses; fees; hostel facilities etc. It also contains brief introduction on homeopathic, ayurvedic, unani, physiotherapy, occupational therapy and naturopathy.
            <br><strong>Pages : 447 + xxiii Price : Rs. 400.00 (Paper Back) US $ : 40 ISBN : 81-7520-106-1</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Tryst With Agriculture Education - 2009 (Academics And Beyond) </h1>
         <p class="underdiv">
            The Tryst with Agriculture Education, second volume in the series, Academics and Beyond. The new volume presents convocation addresses delivered in the universities, university level institutions and Institutes of national importance in Agriculture and Veterinary Sciences, over a period of last 15 years. Convocation addresses constitute treasures of ideas of academicians, social reformers, statesmen and leaders from different walks of life. The precious sermons serve as the storehouse of wisdom. The issues deliberated in this volume range from role of agriculture in the socio-economic status of the country, diversity of agriculture, blending of science and technology in making agriculture a revenue generating enterprise, importance of cattle wealth to the new panorama of agriculture. These serve as guidelines for the future generation. This book may be use for planners, implementers and researchers and shall be a link between traditional agricultural wisdom and the futuristic approach of agricultural development.
            <br><strong>Pages : 277 + xxii Price : Rs. 350.00 (Paper Back) Rs. 450.00 (Hand Bound) US $ : 45 (Hand Bound) ISBN : 81-7520-108-8</strong>
