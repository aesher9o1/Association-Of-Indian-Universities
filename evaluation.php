         <p class="underdiv" >
         The Evaluation Division is the foundation pillar of AIU, embarked at the time of its inception in 1925 to fulfill its most important objective of assisting universities individuals equivalence for their degrees from Foreign universities.<br><br>
         AIU is vested with the power of according academic equivalence to the degrees obtained from the accredited universities outside India for admission to higher studies in Indian Universities as well as employment. The Division provides expert assistance the status of foreign qualifications to the Students, Universitiesagencies including Ministries of the Government of India, etc.<br><br><a id ="bro"></a>
         The Division also provides assistance to evaluation/accrediting agencies of the foreign countries. The Division helps the MHRD to formulate the proposals for Educational Exchange Programmes, between Indian universities and universities of other countries on, Central and State on mutual recognition of educational qualifications. The Division provides expert assistance on the status of foreign qualifications to the Universities, Ministries of the Government of India, Union Public Service Commission and other Central/State agencies, concerning the selection of students have obtained their qualifications from abroad. It also grants academic equivalence to the management programmes of the standalone management institutions approved and given the autonomous status by the AICTE. The Division has, so far, accorded equivalence to Postgraduate Diploma Programme in Management of 69 Institutions The Governing Council of AlU has approved the revised BROCHURE updated up till 30.06.2016 which gives details on the Procedures &amp; Policies adopted by AlU for granting equivalence which is put on AIU's website: <a href="http://www.aiu.ac.in">www.aiu.ac.in</a> The Division has also issued an ADVISORY for the students intending to joinforeign university(s).
