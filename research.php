                Research Division, one of the most dynamic and vibrant divisions of AIU has carved a niche by making significant academic contribution to the higher education system of the country. Established in 1975 as Research Cell with the financial support from the then Ministry of Education, Government of India, has the mandate of acting as a catalyst in the development of higher education. Having elevated to Research Division in 1993, it subsumed its responsibility of providing an intellectual platform to the community of higher education for debating the issues and policies of emerging concern and providing research based recommendation to government of India for enriching the policy framework on higher education. The Division, which takes care of all the academic activities of AIU, received accolade and recognition through its pioneering research work which includes ‘Unit Cost of Higher Education’; ‘Question Bank’; ‘University-Industry Interaction in Higher Education’, Diminishing Autonomy of Indian Universities,  Regulation of Higher Education in India and Abroad, etc.  The Division also facilitates the universities through providing expertise on up-to-date developments in the field of advance teaching and research and on experiments in the management and organization of higher education both in India and abroad. The academic activities of the Division are conducted with the objective of creating a solid theoretical and empirical foundation for policy-making and national reconstruction. The Division organizes a number of academic activities for promoting the cause of higher education.

