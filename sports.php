      <p class="underdiv" style = "background:#F3F3F3;">
      Sports Division, partially funded by the Ministry of Youth Affairs and Sports, functions as Inter University Sports Board (AIU) for promotion of competitive sports in university sector which is full of sporting potential for excellence. It organizes National University Games and Special Coaching/advance Training camps for highly talented and selected athletes in collaboration with member universities, for participation of Indian Universities teams/contingent in National and International Sports Championships.  At present, 206 sporting events are being organized on All India Basis and four zone basis for both men and women sections.<br><br>
      In University Education, Sports play an important role in the development of integrated personality of the youth including – Body, Mind and Spirit.  Accordingly, active participation of University students in competitive sports generates a spirit of healthy competition in daily life too.  It is, therefore, necessary that university youth of country needs to be brought together on a “National Platform” through the media of sports and physical activities with a view to  channelize youthful energy into constructive directions, as a nation building process.
