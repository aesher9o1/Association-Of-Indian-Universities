    <div class="container wow fadeInUp animated cd-headline slide">
        <h2>About</h2>
        <p class="underdiv" style="background-color:#F0F0F0; padding:1em;margin:2em">
            The Governing Council is the executive body of the Association. It manages the day-to-day affairs of the Association. It comprises 23 Vice Chancellors/ Directors including the Secretary General as its Member - Secretary and President as the Chairman. The Governing Council exercises all powers which are not specifically reserved for the General Body and is the final authority for taking decisions in regard to them. The Governing Council decides the issues which have a bearing on educational policies and the issues having policy implications and report to the General Body. With a view to assisting the universities in maintaining and improving their academic standards, the Governing Council may, if found necessary, conduct a review of the working of its members and suggest such actions as it may deem fit in this regard. The Committee presents to the Association each year the annual report, the audited statement of accounts of the Association and the budget estimates of the following financial year. It frames suitable rules and regulations of the Association and confirms that action is taken on all resolutions adopted by the Association. Meetings of the Governing council are held quarterly. If emergency arises, specific issue based meeting may be called.
            <br> To assist the Governing Council, various Committees are constituted to carry out the multifarious work of the Association in an effective and efficient manner. The Committees are named as – Cultural Committee, Equivalence Committee, Finance Committee, Research Committee and Staff Affairs Committee. If require, need-based temporary Committees may be constituted by the Governing Council or the General Body, from time to time.
        </p>
