         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >SELECTIONS FROM UNIVERSITY NEWS</h1>
         <p class="underdiv">
            The 'University News' is a weekly journal carrying a variety of articles on different aspects of higher education. While some of them are of factual nature and local interest, others delineate current national and global thinking. A proper appreciation of various problems of higher education is facilitated if articles on a given theme are read together. The Association has introduced a new series "Selections from University News" in which articles on similar themes are brought together
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Selection 8: "Reforms and Innovations in Higher Education" – (2001)</h1>
         <p class="underdiv">
            The articles included in it review the reforms and innovations introduced in the field of Higher Education in recent years, in an attempt to overcome the inadequacies that have crept into higher education. They collectively indicate how the higher education system in India can be revitalised.
            <br><strong>Price: Deluxe Edition Rs.375/- US $ 40 ISBN: 81-7520-067-7 </strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Selection10: 'Privatisation of Higher Education' – (2003)</h1>
         <p class="underdiv">
            The present volume on 'Privatisation of Higher Education', is the tenth in series. The focus of articles in this volume is mainly upon pros and cons of privatization of education, particularly higher education and a cross-section of views on the issue of private initiatives in higher education are being presented. The themes covered broadly include, Public and Private partnership: A historical perspective and present trends in higher education Privatization of Higher Education; an appraisal Phasing out government subsidy through Self -financing of Programs Emergence of Private Universities; Legal and administrative Framework.
            <br><strong>Price: Deluxe Edition Rs.200/- US $ 25 ISBN: 81-7520-082-7</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >
            Selection 11: Globalization of Indian Higher Education – (2003)
         </h1>
         <p class="underdiv">
            The articles include covering different dimensions of globalization of higher education, which has emerged as an area of increasing concern. The articles deal with a wide variety of issues such as international student mobility, restructuring of academic courses, quality assurance, policy planning and management, institutional collaboration, academic franchising, impact on culture etc. The volume provides a comprehensive reading to the stakeholders of higher education who may find some insights to face the challenges posed by the globalization.
            <br><strong>Price: Deluxe Edition Rs.200/- US $ 25 ISBN: 81-7520-0083-9 </strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Selections : 17 University-Industry Interaction - 2006 </h1>
         <p class="underdiv">
            The volume on University-Industry Interaction is seventeenth in the series Selections from University News. The volume includes articles by eminent scholars focusing on the need of university-industry linkage catering to societal needs, IT revolution and the changing paradigms of higher education. The articles also highlight different approaches of establishing interaction between higher education institutions and industries.
            <br><strong>Pages : 131 Price : Rs. 150.00 (Paper Back) US $ : 20 ISBN : 81-7520-097-9</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Selections:18 Gats And Higher Education - 2006 </h1>
         <p class="underdiv">
            GATS and Higher Education is the eighteenth in the series Selection from University News. The articles included in the volume deal with the genesis and development of WTO and GATS, analyze issues such as impact on society, economy and culture, international response, guidelines for multilateral agreements, policies for promoting, globalization of higher education, modes of operation, access and equity, credit transfer, structural adjustment among the higher learning institutions, academic restructuring, role of regulating agencies etc. The book is useful for students, educational institutions and other stakeholders of higher education.
            <br><strong>Pages : 226 Price : Rs.250.00 (Paper Back) US $ : 30 ISBN : 81-7520-101-0</strong>
            <br>
            <br>
         </p>
         <h1 class="wow fadeInUp animated cd-headline slide subdivison" data-wow-delay=".3s"  >Selections : 19 Financing Higher Education - 2009 </h1>
         <p class="underdiv">
            The volume on Financing Higher Education is the nineteenth in the series Selections from University News. Amidst critical transition in higher education, several questions have been raised, stimulating to find out alternative sources of funding to accelerate and remodel by redefining the role of the universities. The articles trace funding, resource mobilization and, self-sufficiency in higher education in the context of economic reforms. The articles also deal with the tapping of existing resources, policies of central and state government for funding the institutions, private participation, and measures for initiating financial reforms in institutions of higher education in the country.
            <br><strong>Pages : 290 Price : Rs. 400.00 (Paper Back) US $ : 40 ISBN : 81-7520-107-X</strong>
