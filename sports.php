                                        Sports Division, AIU also processes the release of financial assistance, if received from the Sports Ministry to the host universities for the successful conduct of National University Games, every year.  It also scrutinizes the self calculated claim (s) of contesting universities for the award of Maulana Abul Kalam Azad Trophy to be presented to the overall top performing university for promoting  excellence in competitive sports.  The MAKA Trophy is the most prestigious award, which is presented to the Vice Chancellor and Director Sports of winning university by the Hon’ble President of India on the National Sports Day – 29th August, every year<br><br>
                                        Further, the AIU organizes Vizzy Trophy Cricket (M) <a name="minutes" id="minutes"></a> Tournament in collaboration with Board of Control for Cricket in India (BCCI) in which top 4 teams (one from each zone) are selected during zonal cricket tournaments, on the basis of outstanding performance of the players, every year.  Furthermore, the Champion Universities Hockey (Men) Tournament is also organized in association with Jawharlal Nehru Hockey Tournament Society, regularly.  The AIU, being an affiliated member of International University Sports Federation (FISU) arranges visits of Indian Universities teams for participation in World University Games/Championships and also acts as a host to visiting universities teams from other countries as and when required.  <br><br>
                                        Another feather in the cap of the AIU was added when Ministry of Youth Affairs and Sports vide letter No. F9-22/2007-SP.I dated 05-05-2008 elevated the status of Inter-University Sports Board (AIU) to National Sports Promotion Organization (NSPO), on account of its meritorious contribution in the field of University Sports and placed university sports in the “Priority Category of Sports” at par with other National Sports Federations for the purpose of promoting “Olympic Sports”.  Therefore, with the changed role and responsibility, the AIU established 16 Centres of Excellence in various universities for the purpose of talent search and talent promotion on scientific lines and in professional manner to throw-up an adequate amount of potential to feed the national stream.
