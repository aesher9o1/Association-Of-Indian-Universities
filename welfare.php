      <h4 style="text-align:center;margin-left:-0.15em;">DR. JAGDISH NARAIN, FORMER SECRETARY, AIU PASSES AWAY <a href = "#"><br><h5> Condolence of Dr. Jagdish Narain </h5></a></h4>
   </strong>
   <br>
   Former Secretary of the Association of Indian Universities (AIU), Dr. Jagdish Narain passed away on 5th January, 2012 in New Delhi after a brief illness. The AIU staff condoled his death during a Condolence Meeting which was arranged at the AIU House on 9th Jan. which was addressed by Prof. P T Chande, President AIU and ADN Bajpai, Addl. Secy. General. A highly celebrated academic and known for his humane demeanor, Dr Narain gave dynamic leadership to AIU by the construction of its headquarters building, and boosting up all-India universities academic activities during the period 1983-88, particularly in domain of culture & sports and international participation. He held the esteemed positions of Vice Chancellor of University of Roorkee and Director, Water Resources Development Training Centre between 1977-1982, during which period he made valuable contributions to the academic and administrative development of the University. Professor Narain joined as Lecturer at the University of Roorkee in 1953. An eminent researcher and teacher of great ability, Professor Narain rendered meritorious service in engineering education for over three decades. During his long and distinguished career, Professor Narain taught courses at the undergraduate and postgraduate levels, and modernized laboratory facilities for research, particularly in the upcoming field of Geotechnical Engineering. His illustrious career includes working as a consultant to national and international bodies, and proving to be a think-tank to various consultancy firms, notably his keen interest in training of serving engineers in computer applications in engineering design and management projects. He was a World Bank consultant for Water and Land Management Institutes, which also had support from USAID for several years. His administrative and technical acumen helped AICTE,USAID and UNDP in the satisfactory completion of their projects. 
